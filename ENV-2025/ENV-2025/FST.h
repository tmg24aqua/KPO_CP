// FST.h - Конечные автоматы (FST - Finite State Transducer)
//
// Конечные автоматы используются для распознавания различных типов лексем:
// - Идентификаторы (переменные, функции)
// - Ключевые слова (function, main, return и т.д.)
// - Литералы (числа, строки, символы)
// - Операторы и разделители
//
// Структура автомата:
// - NODE: вершина графа переходов
// - RELATION: ребро графа (символ -> следующая вершина)
// - FST: конечный автомат (граф переходов + входная строка)

#pragma once
#include "pch.h"
namespace FST
{
	// Ребро графа переходов: символ -> следующая вершина
	struct RELATION
	{
		unsigned char symbol;				//симпол перехода	
		short nnode;				//номер смежной вершины
		RELATION(
			unsigned char c = 0x00,	//символ перехода
			short ns = 0			//номер смежной вершины
		);
	};

	// Вершина графа переходов конечного автомата
	struct NODE
	{
		short n_relation;			// Количество инцидентных рёбер (переходов из этой вершины)
		RELATION* relations;		// Массив инцидентных рёбер (переходов)
		NODE();
		NODE
		(short n,				//количество инцидентных ребер
			RELATION rel, ...		//список ребер
		);
	};

	// Недетерминированный конечный автомат для распознавания лексем
	struct FST
	{
		unsigned char* string;		// Входная цепочка (строка, завершается 0x00)
		short position;				// Текущая позиция в цепочке
		short nstates;				// Количество состояний автомата
		NODE* nodes;				// Граф переходов: [0] - начальное состояние, [nstate-1] - конечное
		short* rstates;				// Возможные состояния автомата на данной позиции (для недетерминированного автомата)
		FST(short ns, NODE n, ...);
		FST(unsigned char* s, FST& fst);
		FST(
			unsigned char* s,		//цепочка (строка, завершаеся 0х00)
			short ns,				//количество состояний автомата
			NODE n, ...				//список состояний (граф переходов)
		);
	};
	bool execute(
		FST& fst
	);


}	

